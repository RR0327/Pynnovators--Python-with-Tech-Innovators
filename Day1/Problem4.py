"""4. Given text = "PythonProgramming":
• Print first 6 characters
• Print last 4 characters
• Print every second character
• Convert the string to uppercase and lowercase
"""

txt = "PythonProgramming"
print(txt[:6])  # First 6 characters
print(txt[-4:])  # Last 4 characters
print(txt[::2])  # Every second character
print(txt.upper())  # Uppercase
print(txt.lower())  # Lowercase
